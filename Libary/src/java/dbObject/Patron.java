package dbObject;

/**
 *
 * @author CC
 */
public class Patron {
    int id;
    String fname;
    String surname;
    String email;
    String status;

    public Patron() {
    }

    public Patron(int id, String fname, String surname, String email, String status) {
        this.id = id;
        this.fname = fname;
        this.surname = surname;
        this.email = email;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFname() {
        return fname;
    }

    public void setFname(String fname) {
        this.fname = fname;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
}
